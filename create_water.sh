cp npt/water_npt.gro   prdc/
cp npt/water_npt.cpt   prdc/

cd prdc/

gmx grompp -f prdc.mdp -c water_npt.gro -t water_npt.cpt -p topol.top -o water_prdc.tpr
gmx mdrun -deffnm water_prdc

